(ns er-staffing.experiments.evolution-run
  "GA comparisons: single-scenario runs, 2-case lexicase, **5-case lexicase**,
   writes `results/evolution_report.md`, `results/evolution_runs.csv`,
   and `results/lexicase_5case_detail.txt` (per-scenario errors for best genome)."
  (:require [clojure.data.csv :as csv]
            [clojure.java.io :as io]
            [clojure.string :as str]
            [er-staffing.experiments.core :as ex]
            [er-staffing.experiments.scenarios :as scen]))

(defn- write-report!
  [rows md-path csv-path five-case-row]
  (io/make-parents md-path)
  (spit md-path
        (str "# Evolution run report\n\n"
             "Demand is **stress-tuned**: default `week_scenario.edn` uses higher hourly λ so waits are usually **non-zero** unless staffing is large.\n\n"
             "Generated by `er-staffing.experiments.evolution-run`.\n\n"
             "## Runs\n\n"
             "| Run | Scenarios | Mean error | Wait 1st | Wait max | Wait mean | Match (1st) | Budget (1st)? |\n"
             "|-----|-----------|------------|----------|----------|-----------|---------------|----------------|\n"
             (str/join "\n"
                       (map (fn [r]
                              (format "| %s | %s | %.6f | %.6f | %.6f | %.6f | %.4f | %s |"
                                      (:label r)
                                      (:scenario-labels r)
                                      (double (:mean-error-best r))
                                      (double (get-in r [:summary :avg-wait-time] 0))
                                      (double (:avg-wait-max r 0))
                                      (double (:avg-wait-mean r 0))
                                      (double (get-in r [:summary :specialty-matching-rate] 0))
                                      (str (get-in r [:summary :within-budget?]))))
                            rows))
             (when five-case-row
               (str "\n\n## 5-case lexicase (per-scenario fitness of best genome)\n\n"
                    "Scenario order: baseline synthetic -> real hourly profile -> severe respiratory -> tight budget -> high demand.\n\n"
                    "| Scenario | Error (lower better) |\n"
                    "|----------|------------------------|\n"
                    (str/join "\n"
                              (map (fn [[id err]]
                                     (format "| %s | %.6f |" (str id) (double err)))
                                   (map vector
                                        (:five-case-ids five-case-row)
                                        (:best-errors five-case-row))))))
             "\n\n"
             "## Notes\n\n"
             "- **Synthetic**: `week_scenario.edn` hourly λ.\n"
             "- **Real hourly**: empirical within-day shares from hospital CSV.\n"
             "- **Lexicase (N cases)**: error vector length N; parent selection filters on shuffled case order.\n"
             "- **Tight budget**: `week_case_tight_budget.edn` — same care mix as baseline but **low** `:daily-budget-cap`.\n"
             "- **Severe respiratory**: `week_case_severe_respiratory.edn` — higher λ + long respiratory surge events.\n"
             "- **High demand**: `week_case_high_demand.edn` — very high baseline λ.\n"))
  (with-open [w (io/writer csv-path)]
    (csv/write-csv w
                     (cons ["label" "scenarios" "mean_error" "avg_wait_first" "avg_wait_max"
                            "avg_wait_mean" "avg_queue_len_max" "avg_queue_len_mean"
                            "avg_time_in_system_max" "avg_time_in_system_mean"
                            "match_rate_first" "within_budget_first"
                            "rt" "cardio" "radio" "mh" "physician" "pa" "nurse"]
                           (map (fn [r]
                                  (let [b (:best-staffing r)
                                        s (:summary r)
                                        fw (fn [x] (if (nil? x) "" (format "%.8f" (double x))))]
                                    [(str (:label r))
                                     (:scenario-labels r)
                                     (str (:mean-error-best r))
                                     (fw (get s :avg-wait-time))
                                     (fw (:avg-wait-max r))
                                     (fw (:avg-wait-mean r))
                                     (fw (:avg-queue-len-max r))
                                     (fw (:avg-queue-len-mean r))
                                     (fw (:avg-time-in-system-max r))
                                     (fw (:avg-time-in-system-mean r))
                                     (str (get s :specialty-matching-rate))
                                     (str (get s :within-budget?))
                                     (str (:rt b)) (str (:cardio b)) (str (:radio b)) (str (:mh b))
                                     (str (:physician b)) (str (:pa b)) (str (:nurse b))]))
                                rows))))
  (when five-case-row
    (spit "results/lexicase_5case_detail.txt"
          (str "Best genome (5-case lexicase run):\n"
               (pr-str (:best-staffing five-case-row))
               "\n\nPer-scenario errors (same order as scenarios vector):\n"
               (pr-str (:best-errors five-case-row))
               "\n\nScenario ids:\n"
               (pr-str (:five-case-ids five-case-row))))))

(defn -main
  [& _]
  (let [cfg (ex/load-ga-config)
        syn (scen/week-v1)
        real (scen/week-v1-real-hourly)
        syn-rand (scen/with-random-events syn)
        sev (scen/week-severe-respiratory)
        tight (scen/week-tight-budget)
        high (scen/week-high-demand)
        report-opts (merge (ex/merge-ga-opts cfg :ga-defaults)
                           {:bounds (:genome-bounds cfg)
                            :population-size 16
                            :generations 8
                            :replications 2
                            :seed 42
                            :selection :lexicase})
        five-opts (merge report-opts
                         {:population-size 10
                          :generations 5
                          :replications 2
                          :seed 43})
        r1 (ex/run-ga-experiment "1-ga-synthetic-only" [syn] report-opts)
        r2 (ex/run-ga-experiment "2-ga-real-hourly-only" [real] report-opts)
        r3 (ex/run-ga-experiment "3-ga-lexicase-2-cases" [syn real] report-opts)
        r4 (ex/run-ga-experiment "4-ga-synthetic-random-events" [syn-rand] report-opts)
        r5 (ex/run-ga-experiment "5-ga-lexicase-5-cases"
                                 [syn real sev tight high]
                                 five-opts)
        five-detail (assoc r5
                           :five-case-ids (mapv #(str (:scenario-id %)) [syn real sev tight high]))
        rows [(assoc r1 :scenario-labels "week-v1")
              (assoc r2 :scenario-labels "week-v1-real-hourly")
              (assoc r3 :scenario-labels "week-v1+real-hourly")
              (assoc r4 :scenario-labels "week-v1-rand-events")
              (assoc r5 :scenario-labels "5-case:baseline+real+sevResp+tightBudget+highDem")]]
    (write-report! rows "results/evolution_report.md" "results/evolution_runs.csv" five-detail)
    (println "Wrote results/evolution_report.md, results/evolution_runs.csv, results/lexicase_5case_detail.txt")
    (doseq [r rows]
      (println (:label r) "mean-error" (:mean-error-best r) "staffing" (:best-staffing r)))))

